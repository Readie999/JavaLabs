
/**
 * Purpose: To get the counter to decrement and print the corresponding values
 *
 * Description: Print message "value" with the value going from 9 to 0.
 *
 * @author Nick
 *
 * @version Date: 2 Oct 2015 13:03:34
 */
public class ExerciseFivePartThree {

	public static void main( String [ ] args ) {
		// print the message ”message” along with the value of the counter
		for (int counter = 9; counter >= 0; counter--) {
		System.out.println("message" + counter);
		}
		}
}
